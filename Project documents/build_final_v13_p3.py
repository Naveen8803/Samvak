# ── 6.4 grammar_helper.py ─────────────────────────────────────────────────────
subhead("6.4 grammar_helper.py — Gemini Prompt for ISL OSV + Local Fallback")
body(
    'grammar_helper.py provides both the Gemini API prompt construction for ISL OSV '
    'gloss generation and a fully offline local rule-based fallback used when the API '
    'is unavailable. The local engine applies: contraction expansion (I\'m → I AM), '
    'article and auxiliary verb removal, time-marker elevation to sentence-front, '
    'WH-word movement to sentence-end, and uppercase conversion of all tokens. '
    'This deterministic fallback ensures the system degrades gracefully without '
    'Gemini API access.'
)
code_block(
    '"""grammar_helper.py — ISL grammar transformation engine."""\n'
    '\n'
    '_DROP_WORDS  = {"a", "an", "the", "is", "am", "are", "was", "were",\n'
    '                "be", "been", "being", "do", "does", "did"}\n'
    '_WH_WORDS    = {"what", "where", "when", "why", "how", "who", "which"}\n'
    '_TIME_MARKERS= {"today", "tomorrow", "yesterday", "now", "later",\n'
    '                "morning", "evening", "night", "monday", "tuesday",\n'
    '                "wednesday", "thursday", "friday", "saturday", "sunday"}\n'
    '_CONTRACTIONS= {"i\'m":"i am","you\'re":"you are","he\'s":"he is",\n'
    '                "she\'s":"she is","it\'s":"it is","we\'re":"we are",\n'
    '                "they\'re":"they are","i\'ve":"i have","don\'t":"do not",\n'
    '                "doesn\'t":"does not","can\'t":"cannot","won\'t":"will not"}\n'
    '\n'
    'def _expand_contractions(text: str) -> str:\n'
    '    for contraction, expanded in _CONTRACTIONS.items():\n'
    '        text = text.lower().replace(contraction, expanded)\n'
    '    return text\n'
    '\n'
    'def english_to_isl_glosses(text: str) -> list:\n'
    '    """Convert English sentence to ISL gloss list using local rules.\n'
    '       Example: "What is your name?" -> ["YOUR", "NAME", "WHAT"]\n'
    '    """\n'
    '    text    = _expand_contractions(text.lower().strip("?.!,"))\n'
    '    words   = text.split()\n'
    '    time_markers, wh_words, content = [], [], []\n'
    '    for w in words:\n'
    '        if w in _DROP_WORDS:    continue          # drop articles/auxiliaries\n'
    '        elif w in _TIME_MARKERS: time_markers.append(w)  # front\n'
    '        elif w in _WH_WORDS:    wh_words.append(w)       # end\n'
    '        else:                   content.append(w)\n'
    '    ordered = time_markers + content + wh_words   # ISL OSV order\n'
    '    return [w.upper() for w in ordered if w]      # uppercase tokens\n'
    '\n'
    'def build_gemini_prompt(english_text: str) -> str:\n'
    '    """Build the system prompt sent to Gemini for ISL OSV gloss."""\n'
    '    return (\n'
    '        "You are an expert in Indian Sign Language (ISL) grammar.\\n"\n'
    '        "Convert the English sentence to ISL gloss using these rules:\\n"\n'
    '        "1. Use Object-Subject-Verb (OSV) word order.\\n"\n'
    '        "2. Drop all articles: a, an, the.\\n"\n'
    '        "3. Drop auxiliary verbs: is, am, are, was, were, be, been.\\n"\n'
    '        "4. Move WH-words (what, where, when, why, how) to sentence end.\\n"\n'
    '        "5. Place time markers (today, tomorrow, yesterday) at sentence start.\\n"\n'
    '        "6. Return ONLY a JSON array of UPPERCASE gloss strings. No explanation.\\n"\n'
    '        f"English: {english_text}\\n"\n'
    '        \'Expected format: ["TOKEN1", "TOKEN2", "TOKEN3"]\'\n'
    '    )'
)
