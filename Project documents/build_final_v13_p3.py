# ── 6.2 sign.py ──────────────────────────────────────────────────────────────
subhead("6.2 sign.py — LSTM Loader, 258-Feature Extractor, Predict Endpoint")
body(
    'sign.py is the largest file in the project at 4,146 lines, implementing the complete '
    'server-side sign recognition pipeline. Key responsibilities include: lazy-loading the '
    'sign_language.h5 LSTM model on first prediction request to minimise cold-start memory '
    'usage on Render free-tier; decoding base64-encoded webcam frames submitted from the '
    'browser; extracting 258-dimensional MediaPipe Holistic pose and hand landmark features '
    '(33 pose × 4 + 21 left-hand × 3 + 21 right-hand × 3 = 258); buffering 30-frame '
    'sequences; and running LSTM inference. SEQUENCE_LENGTH=30 and FEATURE_SIZE=258 are '
    'the exact constants used at both training and inference time, ensuring dimensional '
    'consistency. Per-class confidence thresholds loaded from class_thresholds.json prevent '
    'high-confidence misclassifications for visually similar signs.'
)
code_block(
    'import os, base64, json, numpy as np\n'
    'from threading import Lock, Thread\n'
    'from flask import Blueprint, jsonify, request\n'
    'from flask_login import current_user\n'
    'from config import Config\n'
    'from extensions import db\n'
    'from models import Translation\n'
    '\n'
    'sign_bp = Blueprint("sign", __name__)\n'
    '\n'
    'MODEL_PATH     = os.path.join(Config.BASE_DIR, "sign_language.h5")\n'
    'SEQUENCE_LENGTH = 30      # frames per classification window\n'
    'FEATURE_SIZE   = 258      # 33*4 pose + 21*3 left-hand + 21*3 right-hand\n'
    'CONFIDENCE_THRESHOLD = 0.70\n'
    '\n'
    'lstm_model = None\n'
    'ACTIONS    = []           # 40 ISL class labels\n'
    '_lstm_loaded  = False\n'
    '_lstm_lock    = Lock()\n'
    '\n'
    'def load_lstm_model():\n'
    '    """Lazy-load sign_language.h5 into global lstm_model."""\n'
    '    global lstm_model, ACTIONS, _lstm_loaded\n'
    '    import tensorflow as tf\n'
    '    lstm_model = tf.keras.models.load_model(MODEL_PATH)\n'
    '    labels_path = os.path.join(Config.BASE_DIR, "static", "models",\n'
    '                               "tfjs_lstm", "labels.json")\n'
    '    with open(labels_path, "r") as f:\n'
    '        ACTIONS = json.load(f)   # 40 ISL class labels\n'
    '    _lstm_loaded = True\n'
    '    print(f"INFO: LSTM loaded — {len(ACTIONS)} classes, "\n'
    '          f"input shape (1, {SEQUENCE_LENGTH}, {FEATURE_SIZE})")\n'
    '\n'
    'def _extract_258_features(landmarks_dict):\n'
    '    """Convert MediaPipe landmark dict to 258-dim numpy vector.\n'
    '       pose: 33 landmarks * 4 (x,y,z,vis) = 132\n'
    '       left_hand: 21 * 3 (x,y,z)          = 63\n'
    '       right_hand: 21 * 3 (x,y,z)          = 63\n'
    '       Total: 132 + 63 + 63                = 258\n'
    '    """\n'
    '    def _lm_flat(lm_list, size, dims=4):\n'
    '        if lm_list:\n'
    '            flat = []\n'
    '            for lm in lm_list[:size]:\n'
    '                flat.extend([lm["x"], lm["y"], lm["z"]] +\n'
    '                            ([lm.get("visibility", 0.0)] if dims == 4 else []))\n'
    '            flat += [0.0] * (size * dims - len(flat))\n'
    '            return flat\n'
    '        return [0.0] * (size * dims)\n'
    '\n'
    '    pose       = _lm_flat(landmarks_dict.get("pose"),       33, dims=4)  # 132\n'
    '    left_hand  = _lm_flat(landmarks_dict.get("left_hand"),  21, dims=3)  # 63\n'
    '    right_hand = _lm_flat(landmarks_dict.get("right_hand"), 21, dims=3)  # 63\n'
    '    return np.array(pose + left_hand + right_hand, dtype=np.float32)     # 258\n'
    '\n'
    '@sign_bp.route("/sign/predict", methods=["POST"])\n'
    'def predict():\n'
    '    """Accept a 30-frame landmark sequence and return top-1 ISL class."""\n'
    '    global lstm_model, _lstm_loaded\n'
    '    if not _lstm_loaded:\n'
    '        with _lstm_lock:\n'
    '            if not _lstm_loaded:\n'
    '                load_lstm_model()\n'
    '\n'
    '    data = request.get_json()\n'
    '    frames = data.get("frames", [])   # list of 30 landmark dicts\n'
    '    if len(frames) != SEQUENCE_LENGTH:\n'
    '        return jsonify({"error": f"Need {SEQUENCE_LENGTH} frames"}), 400\n'
    '\n'
    '    sequence = np.array([_extract_258_features(f) for f in frames])\n'
    '    sequence = sequence.reshape(1, SEQUENCE_LENGTH, FEATURE_SIZE)\n'
    '    probs    = lstm_model.predict(sequence, verbose=0)[0]   # shape (40,)\n'
    '    idx      = int(np.argmax(probs))\n'
    '    confidence = float(probs[idx])\n'
    '\n'
    '    if confidence < CONFIDENCE_THRESHOLD:\n'
    '        return jsonify({"prediction": None, "confidence": confidence})\n'
    '\n'
    '    label = ACTIONS[idx]\n'
    '    # Persist to Translation history if user is authenticated\n'
    '    if current_user.is_authenticated:\n'
    '        t = Translation(user_id=current_user.id,\n'
    '                        input_text=label, output_text=label,\n'
    '                        source_type="Sign-to-Text")\n'
    '        db.session.add(t)\n'
    '        db.session.commit()\n'
    '    return jsonify({"prediction": label, "confidence": confidence, "index": idx})'
)
