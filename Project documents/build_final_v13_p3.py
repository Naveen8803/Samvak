# ── 6.7 isign_retrieval.py ────────────────────────────────────────────────────
subhead("6.7 isign_retrieval.py — Embedding and Cosine Similarity Query")
body(
    'isign_retrieval.py manages the 132 MB cosine similarity index '
    '(isign_retrieval_index.npz) built from the 14,674 iSign video clip embeddings '
    'across 101 phrase folders. At startup, ensure_isign_retrieval_index() loads the '
    'numpy compressed archive into memory. The query_index() function converts a raw '
    '30-frame landmark sequence to a mean-pooled 258-dimensional embedding, computes '
    'cosine similarity against all stored embeddings, and returns the top-k matching '
    'clip metadata (phrase label, video URL, similarity score). This powers both the '
    'ISL dictionary search and the sign recognition fallback when LSTM confidence '
    'is below the configured threshold.'
)
code_block(
    'import numpy as np, os\n'
    'from typing import List, Dict\n'
    '\n'
    'DEFAULT_INDEX_PATH = "isign_retrieval_index.npz"\n'
    'DEFAULT_META_PATH  = "isign_retrieval_meta.json"\n'
    '\n'
    '_index_embeddings = None    # shape (N, 258) float32\n'
    '_index_meta       = []      # list of {phrase, clip_id, url}\n'
    '\n'
    'def ensure_isign_retrieval_index(index_path: str, meta_path: str) -> bool:\n'
    '    """Load the 132 MB isign_retrieval_index.npz into memory."""\n'
    '    global _index_embeddings, _index_meta\n'
    '    if _index_embeddings is not None:\n'
    '        return True\n'
    '    if not os.path.exists(index_path):\n'
    '        print(f"WARN: iSign retrieval index not found at {index_path}")\n'
    '        return False\n'
    '    data = np.load(index_path, allow_pickle=False)\n'
    '    _index_embeddings = data["embeddings"].astype(np.float32)  # (N, 258)\n'
    '    import json\n'
    '    with open(meta_path, "r") as f:\n'
    '        _index_meta = json.load(f)\n'
    '    print(f"INFO: iSign index loaded — {len(_index_meta)} clips, "\n'
    '          f"embeddings shape {_index_embeddings.shape}")\n'
    '    return True\n'
    '\n'
    'def sequence_to_embedding(sequence: np.ndarray) -> np.ndarray:\n'
    '    """Mean-pool a (30, 258) sequence to a 258-dim embedding vector.\n'
    '       Normalise to unit length for cosine similarity computation.\n'
    '    """\n'
    '    emb = sequence.mean(axis=0).astype(np.float32)  # (258,)\n'
    '    norm = np.linalg.norm(emb)\n'
    '    return emb / (norm + 1e-8)\n'
    '\n'
    'def query_index(sequence: np.ndarray, top_k: int = 5) -> List[Dict]:\n'
    '    """Compute cosine similarity and return top-k matches.\n'
    '       Returns list of {phrase, url, similarity} sorted by similarity desc.\n'
    '    """\n'
    '    if _index_embeddings is None:\n'
    '        return []\n'
    '    query_vec = sequence_to_embedding(sequence)            # (258,)\n'
    '    # Dot product with pre-normalised index embeddings = cosine similarity\n'
    '    sims = _index_embeddings @ query_vec                   # (N,)\n'
    '    top_idx = np.argsort(sims)[::-1][:top_k]\n'
    '    return [\n'
    '        {**_index_meta[i], "similarity": float(sims[i])}\n'
    '        for i in top_idx\n'
    '    ]'
)
body('[INSERT SCREENSHOT HERE — Caption: ISL Dictionary page showing search query "hello", retrieved top-3 matching iSign clips with similarity scores (0.94, 0.91, 0.87) and clip thumbnails displayed]')

# ── 6.8 sign.js ───────────────────────────────────────────────────────────────
subhead("6.8 sign.js — TF.js Load, MediaPipe Extract, 30-Frame Buffer, Classify")
body(
    'sign.js (~1,600 lines) is the complete browser-side ML pipeline. On page load, '
    'it initialises MediaPipe Holistic and loads the TF.js LSTM model bundle from '
    '/static/models/tfjs_lstm/model.json. During animation frame callbacks, it extracts '
    '258-dimensional landmark vectors from each webcam frame, buffers the last 30 vectors '
    'in a circular array, runs TF.js prediction when the buffer is full, applies per-class '
    'confidence thresholds, and displays the result. A minimum-motion filter prevents '
    'static-hand false positives by checking landmark velocity across consecutive frames.'
)
code_block(
    '// sign.js — Browser-side LSTM sign recognition pipeline\n'
    '// Part of Samvak (Multi-Lingual Sign Language Translator)\n'
    '\n'
    'const SEQUENCE_LENGTH = 30;    // frames per prediction window\n'
    'const FEATURE_SIZE    = 258;   // 33*4 pose + 21*3 left + 21*3 right\n'
    'const CONFIDENCE_THR  = 0.70;  // minimum confidence to accept prediction\n'
    'const MIN_MOTION      = 0.002; // minimum landmark velocity to trigger inference\n'
    '\n'
    'let tfModel   = null;          // TF.js LayersModel\n'
    'let holistic  = null;          // MediaPipe Holistic instance\n'
    'let labels    = [];            // 40 ISL class label strings\n'
    'let frameBuffer = [];          // circular buffer of 258-dim Float32Arrays\n'
    '\n'
    'async function loadModel() {\n'
    '  // Load TF.js LSTM from tfjs_lstm/model.json\n'
    '  tfModel = await tf.loadLayersModel("/static/models/tfjs_lstm/model.json");\n'
    '  const res = await fetch("/static/models/tfjs_lstm/labels.json");\n'
    '  labels = await res.json();   // 40 ISL class strings\n'
    '  console.log(`LSTM loaded: ${labels.length} classes, ` +\n'
    '              `input (1, ${SEQUENCE_LENGTH}, ${FEATURE_SIZE})`);\n'
    '}\n'
    '\n'
    'function extractFeatures(results) {\n'
    '  // Extract 258-dim vector from MediaPipe Holistic results\n'
    '  function landmarks(lmList, count, dims) {\n'
    '    const vec = new Array(count * dims).fill(0);\n'
    '    if (lmList) {\n'
    '      lmList.slice(0, count).forEach((lm, i) => {\n'
    '        vec[i * dims]     = lm.x;\n'
    '        vec[i * dims + 1] = lm.y;\n'
    '        vec[i * dims + 2] = lm.z;\n'
    '        if (dims === 4) vec[i * dims + 3] = lm.visibility || 0;\n'
    '      });\n'
    '    }\n'
    '    return vec;\n'
    '  }\n'
    '  const pose  = landmarks(results.poseLandmarks,       33, 4); // 132\n'
    '  const lhand = landmarks(results.leftHandLandmarks,   21, 3); // 63\n'
    '  const rhand = landmarks(results.rightHandLandmarks,  21, 3); // 63\n'
    '  return new Float32Array([...pose, ...lhand, ...rhand]);       // 258\n'
    '}\n'
    '\n'
    'async function onResults(results) {\n'
    '  const features = extractFeatures(results);  // Float32Array(258)\n'
    '\n'
    '  // Motion filter: skip near-static frames\n'
    '  if (frameBuffer.length > 0) {\n'
    '    const last = frameBuffer[frameBuffer.length - 1];\n'
    '    const motion = features.reduce((sum, v, i) =>\n'
    '      sum + Math.abs(v - last[i]), 0) / FEATURE_SIZE;\n'
    '    if (motion < MIN_MOTION) return;\n'
    '  }\n'
    '\n'
    '  frameBuffer.push(features);\n'
    '  if (frameBuffer.length > SEQUENCE_LENGTH)\n'
    '    frameBuffer.shift();  // circular buffer\n'
    '\n'
    '  if (frameBuffer.length === SEQUENCE_LENGTH && tfModel) {\n'
    '    // Build (1, 30, 258) tensor and run inference\n'
    '    const flat   = new Float32Array(SEQUENCE_LENGTH * FEATURE_SIZE);\n'
    '    frameBuffer.forEach((f, i) => flat.set(f, i * FEATURE_SIZE));\n'
    '    const tensor = tf.tensor3d(flat, [1, SEQUENCE_LENGTH, FEATURE_SIZE]);\n'
    '    const probs  = tfModel.predict(tensor);   // shape (1, 40)\n'
    '    const data   = await probs.data();        // Float32Array of 40 probs\n'
    '    tensor.dispose();  probs.dispose();\n'
    '\n'
    '    const idx        = data.indexOf(Math.max(...data));\n'
    '    const confidence = data[idx];\n'
    '    if (confidence >= CONFIDENCE_THR) {\n'
    '      const label = labels[idx];\n'
    '      displayPrediction(label, confidence);\n'
    '      sendToFlask(label, confidence);  // POST /sign/predict for translation+TTS\n'
    '    }\n'
    '  }\n'
    '}'
)
body('[INSERT SCREENSHOT HERE — Caption: Browser DevTools console showing TF.js LSTM model loaded successfully with "40 classes, input (1, 30, 258)" message and MediaPipe Holistic initialised, with live webcam feed visible in background]')

