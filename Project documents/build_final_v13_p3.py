# ── 6.3 speech.py ─────────────────────────────────────────────────────────────
subhead("6.3 speech.py — STT, Gemini Gloss, Avatar Token Generation")
body(
    'speech.py handles the entire speech-to-sign pipeline. Uploaded audio is converted '
    'from WebM to 16 kHz mono WAV using pydub, transcribed via Google STT '
    '(SpeechRecognition), pre-processed by grammar_helper.py for basic ISL rule application, '
    'then refined by the Gemini API (gemini-1.5-flash) to produce a JSON array of ISL gloss '
    'tokens in correct OSV order. The function also uses the iSign retrieval index to '
    'look up video clip URLs for each gloss token, providing the browser with both '
    'avatar animation data and ISL dictionary clip references.'
)
code_block(
    'import os, json, tempfile\n'
    'import speech_recognition as sr\n'
    'from pydub import AudioSegment\n'
    'from flask import Blueprint, request, jsonify\n'
    'from grammar_helper import english_to_isl_glosses\n'
    '\n'
    'speech_bp = Blueprint("speech", __name__)\n'
    '\n'
    'def _transcribe_audio(audio_path: str) -> str:\n'
    '    """Convert any audio format to WAV, then run Google STT."""\n'
    '    wav_path = audio_path.replace(os.path.splitext(audio_path)[1], ".wav")\n'
    '    audio = AudioSegment.from_file(audio_path)\n'
    '    audio = audio.set_channels(1).set_frame_rate(16000)\n'
    '    audio.export(wav_path, format="wav")\n'
    '\n'
    '    recognizer = sr.Recognizer()\n'
    '    with sr.AudioFile(wav_path) as source:\n'
    '        audio_data = recognizer.record(source)\n'
    '    return recognizer.recognize_google(audio_data, language="en-IN")\n'
    '\n'
    'def _gloss_via_gemini(english_text: str) -> list:\n'
    '    """Call Gemini API to convert English sentence to ISL OSV gloss list."""\n'
    '    import google.generativeai as genai\n'
    '    genai.configure(api_key=os.environ.get("GEMINI_API_KEY", ""))\n'
    '    model = genai.GenerativeModel("gemini-1.5-flash")\n'
    '    prompt = (\n'
    '        "Convert the following English sentence to Indian Sign Language (ISL) gloss.\\n"\n'
    '        "Rules: Use Object-Subject-Verb order. Drop articles (a, an, the).\\n"\n'
    '        "Drop auxiliary verbs (is, am, are, was, were). Move WH-words to end.\\n"\n'
    '        "Return ONLY a JSON array of uppercase gloss tokens. No explanation.\\n"\n'
    '        f"English: {english_text}"\n'
    '    )\n'
    '    response = model.generate_content(prompt)\n'
    '    raw = response.text.strip()\n'
    '    # Parse JSON array from response\n'
    '    start = raw.find("[");  end = raw.rfind("]") + 1\n'
    '    return json.loads(raw[start:end]) if start != -1 else []\n'
    '\n'
    '@speech_bp.route("/speech/upload", methods=["POST"])\n'
    'def upload_speech():\n'
    '    """Receive audio, transcribe, generate ISL gloss tokens."""\n'
    '    if "audio" not in request.files:\n'
    '        return jsonify({"error": "No audio file provided"}), 400\n'
    '    audio_file = request.files["audio"]\n'
    '    with tempfile.NamedTemporaryFile(suffix=".webm", delete=False) as tmp:\n'
    '        audio_file.save(tmp.name)\n'
    '        tmp_path = tmp.name\n'
    '    try:\n'
    '        transcription = _transcribe_audio(tmp_path)\n'
    '        gloss_tokens  = _gloss_via_gemini(transcription)\n'
    '        if not gloss_tokens:\n'
    '            # Fall back to local grammar engine\n'
    '            gloss_tokens = english_to_isl_glosses(transcription)\n'
    '        return jsonify({"transcription": transcription,\n'
    '                        "gloss": gloss_tokens})\n'
    '    except Exception as exc:\n'
    '        return jsonify({"error": str(exc)}), 500\n'
    '    finally:\n'
    '        os.unlink(tmp_path)'
)
